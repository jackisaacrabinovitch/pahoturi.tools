OutofIPA = function(x){
    return x.
    replace(/ʈ͡ʂ|tʂ/gi,"").
    replace(/dʐ|ɖ͡ʐ/gi,"").
    replace(/k͡pʷ|kpʷ/gi,"").
    replace(/ɡ͡bʷ|ɡbʷ/gi,"").
    replace(/ɡ/gi,"").
    replace(/ʃ/gi,"").
    replace(/ʒ/gi,"").
    replace(/ɲ/gi,"").
    replace(/ŋ/gi,"").
    replace(/r|ɹ/gi,"").
    replace(/ɽ/gi,"").
    replace(/ʎ/gi,"").
    replace(/ɪ/gi,"").
    replace(/j/gi,"").
    replace(/æ/gi,"").
    replace(/ə/gi,"").
    replace(/ɐ|a/gi,"")
}